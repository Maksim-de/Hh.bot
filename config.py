def extract_text_from_pdf(file: bytes) -> str:
    """–ü—Ä–∏–Ω–∏–º–∞–µ—Ç PDF –∫–∞–∫ bytes, –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–∑–≤–ª–µ—á–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç."""
    reader = PdfReader(io.BytesIO(file))
    text = " ".join([page.extract_text() for page in reader.pages])
    return text



